{
    use HasFactory;

    protected $fillable = [
        'name',
        'description',
        'is_active'
    ];

    protected $casts = [
        'is_active' => 'boolean',
    ];

    /**
     * RelaciÃ³n con las competencias
     */
    public function competitions()
    {
        return $this->hasMany(Competition::class);
    }
}
